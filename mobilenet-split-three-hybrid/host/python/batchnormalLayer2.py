        self.m_nInputNum = nInputNum
        self.m_nInputWidth = nInputWidth
        self.m_nInputSize = self.m_nInputWidth * self.m_nInputWidth
        self.m_pfOutput = [0.0] * (self.m_nInputNum * self.m_nInputSize)
        self.m_pfMean = [0.0] * self.m_nInputNum
